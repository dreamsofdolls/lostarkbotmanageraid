// Web companion locale dictionaries. Mirror of the bot-side
// bot/locales/{vi,jp,en}.js but ESM-shaped for browser consumption.
// Same key namespacing convention - nested objects, dotted access via t().
//
// The active language is passed in via the URL token (encoded by
// mintToken on the bot side based on the user's /raid-language pref);
// web/i18n.js reads it at boot. Default falls back to "vi" matching
// User.language schema default.
//
// Boss names ("Brelshaza, Ember in the Ashes", "Archdemon Kazeros")
// stay English across all locales because LOA Logs writes them
// verbatim to encounters.db - we don't translate proper nouns the
// game itself doesn't translate. Raid+mode LABELS (the bucket
// header text "Act 4 Hard") are localized.

